export function getLanguage(setting: string): Language {
  if (setting === "ru" || setting === "en") {
    return setting;
  }
  // Auto detect
  const browserLang = navigator.language.toLowerCase();
  if (browserLang.startsWith("ru")) return "ru";
  return "en";
}

